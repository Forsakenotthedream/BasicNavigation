<h1>Lenguajes del lado del cliente</h1>
</br>
<img src="imagenes/imagen-varios5.JPG">
</br>
<p>

HTML es un lenguaje que se basa en etiquetas que le indican al navegador dónde colocar cada texto, imágen, vídeo,… y la forma que tendrán estos al ser colocados en la página. Estas etiquetas se pueden utilizar para definir la forma o estilo que le queremos aplicar a nuestro documento.

</p>
<!--2.javas-->
</br>
<img src="imagenes/imagen-varios6.JPG">
</br>
<p>

JavaScript se utiliza para crear pequeñas aplicaciones que se encargan de realizar acciones dentro de la web. Es el propio navegador el que soporta la carga de su procesamiento. La principal ventaja de utilizar applets consiste en que son mucho menos dependientes del navegador que los scripts en Javascript, incluso independientes del sistema operativo del ordenador donde se ejecutan. Además, Java es más potente que Javascript, por lo que el número de aplicaciones de los applets podrá ser mayor.

</p>
<!--3.applets-->
</br>
<img src="imagenes/imagen-varios7-192x347.JPG">
</br>
<p>

Las Applets de Java es otra manera de incluir código a ejecutar en los clientes que visualizan una web. Son pequeñas aplicaciones que se transfieren con las webs y que el navegador ejecuta en la página. Están precompilados, por lo que su forma de responder varía de los de JavaScript, y son más difíciles de programar, pero también son más potentes e independientes del navegador o SSOO que se utilice.

</p>
<!--4.VBScript-->
</br>
<img src="imagenes/imagen-varios8.JPG">
</br>
<p>

VBScript es un lenguaje que también programa scripts, pero sólo es compatible con Internet Explorer, lo cual lo limita ampliamente. Visual Basic Script, como su propio nombre indica, se basa en Visual Basic, pero es una versión reducida de mismo, por lo que también se limita en este aspecto.

</p>
<!--5.CSS-->
</br>
<img src="imagenes/imagen-varios9.JPG">
</br>
<p>

CSS, es una tecnología que nos permite crear páginas web de una manera más exacta. Gracias a las CSS somos mucho más dueños de los resultados finales de la página, pudiendo hacer muchas cosas que no se podía hacer utilizando solamente HTML, como incluir márgenes, tipos de letra, fondos, colores... Incluso podemos definir nuestros propios estilos en un archivo externo a nuestras páginas; así, si en algún momento queremos cambiar alguno de ellos, automáticamente se nos actualizarán todas las páginas vinculadas de nuestro sitio.

</p>
<!--6.Flash-->
</br>
<img src="imagenes/flash.GIF">
</br>
<p>

Flash es una tecnología, y un programa, para crear efectos especiales en páginas web. Con Flash también conseguimos hacer páginas dinámicas del lado del cliente. Flash en realidad no es un lenguaje; Sin embargo, si tuvieramos que catalogarlo en algún sitio quedaría dentro del ámbito de las páginas dinámicas de cliente.

</p>
