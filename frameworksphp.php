<p>
  <h1>Slim</h1>
  </br>
  <img src="imagenes/Slim.PNG">
  </br>
  Es un Micro Framework PHP que posee una sintaxis muy sencilla. slim frameworkSu menor tamaño es debido a que cuenta con menor cantidad de elementos preinstalados pero eso sí es igual de eficiente que los de mayor tamaño. Una de las características principales es que se trata de una herramienta de código abierto refinada e instintiva ya que no emplea fragmentos de otros Frameworks. Es ideal para generar un Backend de un proyecto sencillito en PHP para el cual no haga falta escribir del sistema desde cero. Entre algunas de sus funcionalidades; proporciona soporte para mensajes flash, cookies encriptadas…
</p>
</br>
<!--2-->
<p>
  <h1>Zend</h1>
  </br>
  <img src="imagenes/Zend.PNG">
  </br>
  Se trata de un Framework seguro y amoldable a cualquier proyecto independientemente de su tamaño, ya que es muy escalable por eso no es de extrañar que muchas de la grandes empresas lo utilicen. zend frameworkTambién tengo que confesar que se podría decir que es el más complejo puesto que su curva de aprendizaje es bastante difícil pero los resultados merecen la pena. La última versión, Zend Framework 3, la han diseñado con la optimización del rendimiento en mente tratando de hacer la aplicación más amigable y veloz. ¡Ah, se me olvidaba! Es de código abierto.
</p>
</br>
<!--3-->
<p>
  <h1>Yii</h1>
  </br>
  <img src="imagenes/yii.PNG">
  </br>
  Su versión 2.0 lo ha posicionado entre una de las aplicaciones más demandadas compitiendo con el mismísimo Symfony. Esto es gracias a Gii, una herramienta de generación de código de clase que simplifica sustancialmente el desarrollo y a que Yii es muy adecuado para proyectos multiusuarios(estilo CMS o CRM) debido a su fuerte almacenamiento en cache. Pero una de las cosas que más destaca es la comunidad que ha creado podría decir que las más activa de todas.
</p>
</br>
<!--3-->
<p>
  <h1>Sympfony</h1>
  </br>
  <img src="imagenes/Symfony.PNG">
  </br>
  Este Framework se caracteriza por su versatilidad, está formado por un gran conjunto de componentes reutilizables ideales para desarrollar proyecto complicados y de gran envergadura. symfony frameworkUna de las mejores características es el uso de Doctrine como ORM para la generación de entidades. Cuenta con una comunidad muy activa, la cual no para de proponer ideas para realizar mejoras o novedades en las siguientes versiones.
</p>
</br>
<!--3-->
<p>
  <h1>Larabel</h1>
  </br>
  <img src="imagenes/laravel.PNG">
  </br>
  Sin duda alguna es el mejor de los Frameworks en PHP para Backend. ¿Que por qué lo es? Porque se fundamenta en Symfony pero evitando toda su complejidad. Laravel ha sido diseñado para ser excesivamente sencillo tanto de aprender como de utilizar.

  Es la elección más empleada en cualquier empresa de desarrollo de aplicaciones web ya que se logran unos resultados increíbles. Por otro lado posee su propio motor de plantillas(Blade), sistema de migración expresiva, contenedor de controles de inversión… en definitiva herramientas muy poderosas para generar grandes aplicaciones. El equipo de Laravel ha desarrollado un micro Framework, Lumen, para realizar proyectos de menor magnitud.
