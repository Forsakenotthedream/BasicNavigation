<h1>Programacion Web</h1>
</br>
<p>

La programación de los sitios web es una de las disciplinas dentro del mundo de Internet que más se ha desarrollado y no deja de sorprender día a día con las posibilidades que abre y genera, ya que no sólo consigue satisfacer necesidades que se generan, sino que sin la generación de necesidades ofrecen servicios a los usuarios que éstos no habían imaginado.

En principio, el gran desarrollo de Internet se fundamentó en la posibilidad de enlazar a través de hipervículos diferentes páginas web lo que generó la enorme interconexión que es hoy Internet. Esta base que inició esta red, fué desarrollada y generó el lenguaje ícono de las páginas web que es HTML.

Pero, como decíamos, la creatividad humana no tiene límites y lejos de contentarse con el desarrollo del lenguaje HTML, enriqueciéndolo en su sintaxis, aparecieron otro lenguajes, que a su vez desataron una reacción en cadena con respecto a las operaciones que se podían lograr en un sitio web.

De esta forma, apareció el lenguaje Java, que mejoró sustancialmente la interacción con el usuario, de manera que éste anexó una sensación de amigabilidad y usabilidad en Internet, contribuyendo a que su utilización sea mayor. Los servidores, que son los vinculantes, entre nuestro sitio web y los usuarios, comenzaron a adaptarse a estas nuevas tecnologías y lenguajes y mejorar sus servicios, permitiendo que ha través de programas se pudieran generar un diálogo on line con los usuarios de forma
 de que éstos recibieran la información que buscaran más rápido y de forma más eficiente.

</br>
<img src="imagenes/Prograweb.JPG">
</br>

De esta manera de Java nació Java Script, que ha su vez incentivó a la creación de otros lenguajes que apuntaron a el mimso objetivo como PHP, y así constantemente se están buscando mejoras y nuevas alternativas, todas que apuntan a la interacción del usuario con el sitio.

Por todo esto que estamos indicando antes, la programación del sitio web tiene la misma importancia que el diseño web, no se trata de poner uno por encima de otro, los dos se complementan y hacen que la eficiencia del sitio web sea importante.

Está demostrado que los sitios más visitados son los que brindan un servicio más “personalizado” al usuario y esto se logra pura y exclusivamente a través de la programación web, que a través de conocer los gustos y tendencias del usuario, utiliza estos datos para proporcionarle al mismo información que más le interesa.

